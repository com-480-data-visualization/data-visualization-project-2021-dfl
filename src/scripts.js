function loadGasByCountry(country, gas) {
	// prepare the data here
	
	const VALUES = loadedData[country][gas];
	console.log("VALUES: ", VALUES);
	const YEARS = Array.from(Array(29), (elem, index) => 1990 + index);

	/* 
	* convert data into an array where each element is 
	* {'y': value, 'date': year}.
	*/
	var dataArray = [];
	YEARS.forEach(function (year, i) {
		var temp_dict = {};
		temp_dict['y'] = VALUES[i]; 
		temp_dict['date'] = new Date(+year, 0, 1, 0, 0, 0, 0);
		
		dataArray.push(temp_dict);

	})	
	return dataArray;
}


function displayMap() {
    // The svg
    let svg = d3.select("#my_dataviz"),
        width = +svg.attr("width"),
        height = +svg.attr("height");
    
    // Map and projection
    let projection = d3.geoEquirectangular() 
        .scale(100);
        //.center([0,20])
        //.translate([width / 2, height / 2]);
    let path = d3.geoPath().projection(projection);
    // Data and color scale
    let data = d3.map();
    let color = d3.scaleSequential()
                  .domain([0, 500000000])
                  .interpolator(d3.interpolateYlGnBu);
    
    
    // Load external data and boot
    d3.queue()
        .defer(d3.json, "https://raw.githubusercontent.com/holtzy/D3-graph-gallery/master/DATA/world.geojson")
        .defer(d3.csv, "https://raw.githubusercontent.com/holtzy/D3-graph-gallery/master/DATA/world_population.csv", function(d) { data.set(d.code, +d.pop); })
        .await(ready);
    
    // Time slider
    
    var dataTime = d3.range(0, 10).map(function(d) {
    return new Date(1995 + d, 10, 3);
    });
    
    var sliderTime = d3
                    .sliderBottom()
                    .min(d3.min(dataTime))
                    .max(d3.max(dataTime))
                    .step(1000 * 60 * 60 * 24 * 365)
                    .width(600)
                    .tickFormat(d3.timeFormat('%Y'))
                    .tickValues(dataTime)
                    .default(new Date(1998, 10, 3))
    
    var gTime = d3
                .select('div#slider-time')
                .append('svg')
                .attr('width', 1000)
                .attr('height', 100)
                .append('g')
                .attr('transform', 'translate(180,20)');
    
    gTime.call(sliderTime);
    
    
    function ready(error, topo) {
    
        if (error) throw error;
    
        // Draw the map
        svg.append("g") 
        .selectAll("path")
        .data(topo.features)
        .enter()
        .append("path")
            // draw each country
            .attr("d", path)
            // set the color of each country
            .attr("fill", function (d) {
            d.total = data.get(d.id) || 0;
            return color(d.total);
            }).style("stroke", "white");
        }   
    
    // legend function
    
      let title = "GHG emissions in kt",
      tickSize = 6,
      w = 320, 
      h = 44 + tickSize,
      marginTop = 18,
      marginRight = 0,
      marginBottom = 16 + tickSize,
      marginLeft = 0,
      ticks = w / 64,
      tickFormat,
      tickValues
    
      const svg_new = d3.select("#legend")
          .attr("width", w)
          .attr("height", h)
          .attr("viewBox", [0, 0, w, h])
          .style("overflow", "visible")
          .style("display", "block")
          .attr("transform", "translate(" + width/6 + "," + height/6 + ")");
    
      let tickAdjust = g => g.selectAll(".tick line").attr("y1", marginTop + marginBottom - h);
      let x;
    
    
      // Sequential
      if (color.interpolator) {
        x = Object.assign(color.copy()
            .interpolator(d3.interpolateRound(marginLeft, w - marginRight)),
            {range() { return [marginLeft, w - marginRight]; }});
    
        svg_new.append("image")
            .attr("x", marginLeft)
            .attr("y", marginTop)
            .attr("width", w - marginLeft - marginRight)
            .attr("height", h - marginTop - marginBottom)
            .attr("preserveAspectRatio", "none")
            .attr("xlink:href", ramp(color.interpolator()).toDataURL());
    
        // scaleSequentialQuantile doesn’t implement ticks or tickFormat.
        if (!x.ticks) {
          if (tickValues === undefined) {
            const n = Math.round(ticks + 1);
            tickValues = d3.range(n).map(i => d3.quantile(color.domain(), i / (n - 1)));
          }
          if (typeof tickFormat !== "function") {
            tickFormat = d3.format(tickFormat === undefined ? ",f" : tickFormat);
          }
        }
      }
    
      svg_new.append("g")
          .attr("transform", `translate(0,${h - marginBottom})`)
          .call(d3.axisBottom(x)
            .ticks(ticks, typeof tickFormat === "string" ? tickFormat : undefined)
            .tickFormat(typeof tickFormat === "function" ? tickFormat : undefined)
            .tickSize(tickSize)
            .tickValues(tickValues))
          .call(tickAdjust)
          .call(g => g.select(".domain").remove())
          .call(g => g.append("text")
            .attr("x", marginLeft)
            .attr("y", marginTop + marginBottom - h - 6)
            .attr("fill", "currentColor")
            .attr("text-anchor", "start")
            .attr("font-weight", "bold")
            .attr("class", "title")
            .text(title));
    
    function ramp(color, n = 256) {
      var canvas = document.createElement('canvas');
      canvas.width = n;
      canvas.height = 1;
      const context = canvas.getContext("2d");
      for (let i = 0; i < n; ++i) {
        context.fillStyle = color(i / (n - 1));
        context.fillRect(i, 0, 1, 1);
      }
      return canvas;
    }
}


